'''Faça uma condição que compara a variável numero e imprime Número maior ou igual a 30 caso:
- o número seja maior ou igual a 30
- número menor que 30 caso o número seja menor que 30.'''

numero = int(input('Digite um valor: '))

if numero >= 30:
    print('Este número é igual maior ou igual a 30')
else:
    print('Este número é menor que 30')
